import { NextResponse, type NextRequest } from "next/server";
import { createServerClient, type CookieOptions } from "@supabase/ssr";

type CookieToSet = { name: string; value: string; options?: CookieOptions };

// Refreshes the Supabase auth session and gates the HR dashboard.
// Candidate (`/join/...`) and report (`/r/...`) links are public by token.
export async function middleware(request: NextRequest) {
  const { pathname } = request.nextUrl;

  // Only the HR areas and auth pages need the (network) auth check + session
  // refresh. Public pages (landing, candidate /join, report /r, APIs) skip it
  // entirely — saving an auth round-trip on every such request.
  const needsAuth =
    pathname.startsWith("/dashboard") ||
    pathname.startsWith("/interview") ||
    pathname === "/login" ||
    pathname === "/signup";

  if (!needsAuth) return NextResponse.next();

  let response = NextResponse.next({ request });

  const supabase = createServerClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
    {
      cookies: {
        getAll() {
          return request.cookies.getAll();
        },
        setAll(cookiesToSet: CookieToSet[]) {
          cookiesToSet.forEach(({ name, value }) =>
            request.cookies.set(name, value),
          );
          response = NextResponse.next({ request });
          cookiesToSet.forEach(({ name, value, options }) =>
            response.cookies.set(name, value, options),
          );
        },
      },
    },
  );

  const {
    data: { user },
  } = await supabase.auth.getUser();

  // Protected HR areas.
  const isProtected =
    pathname.startsWith("/dashboard") || pathname.startsWith("/interview");

  if (isProtected && !user) {
    const url = request.nextUrl.clone();
    url.pathname = "/login";
    url.searchParams.set("next", pathname);
    return NextResponse.redirect(url);
  }

  // Bounce authed users away from auth pages.
  if ((pathname === "/login" || pathname === "/signup") && user) {
    const url = request.nextUrl.clone();
    url.pathname = "/dashboard";
    return NextResponse.redirect(url);
  }

  return response;
}

export const config = {
  matcher: [
    // Run on everything except static assets.
    "/((?!_next/static|_next/image|favicon.ico|.*\\.(?:svg|png|jpg|jpeg|gif|webp)$).*)",
  ],
};
